import { model, Schema } from "mongoose";

const expenseSchema = new Schema({
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
  amount: {
    type: Number,
    required: true,
  },
});

export default model("Expense", expenseSchema);
